#include "libft.h"

char	*ft_strncpy(char *destination, const char *source, size_t num)
{
	size_t i;

	i = 0;
	while (source[i] && i < num)
	{
		destination[i] = source[i];
		i++;
	}
	while (i < num)
	{
		destination[i] = '\0';
		i++;
	}
	return (destination);
}
